//Activity 1: Using the increment operator '++'

#include<iostream>
using namespace std;

int main ()
{   int a;
    char b;
    a=0, b=0;
    cout<<"\n type-in an integer \t";
    cin>>a; cout<<a; cout<<"\t";
    a++; cout<<a; cout<<"\t";
    a++; cout<<a; cout<<"\t";
    cout<<"\n type in a letter \t";
    cin>>b; cout<<b; cout<<"\t";
    b++; cout<<b; cout<<"\t";
    b++; cout<<b; cout<<"\t";
    return 0;
}

